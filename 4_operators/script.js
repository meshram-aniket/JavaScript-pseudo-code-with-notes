 // Operators in JavaScript
    // 1. Arithmetic 

    var a = 100;
    var b = 10;
    console.log("the value of a + b is: = "+ a + b);
    console.log("the value of a - b is: = "+ (a - b));
    console.log("the value of a * b is: = "+ a * b);
    console.log("the value of a / b is: = "+ a / b);


    // 2. Assignment Operator
    a -= b;
    a += b;
    a *= b;
    a /= b;
    console.log(a)


    // 3. Comparison Operators
    var x = 56;
    var y = 45;
    console.log(x == y);
    console.log(x != y);
    console.log(x >= y);
    console.log(x <= y);
    console.log(x < y);
    console.log(x > y);



    // 4. Logical Operators
    // AND
    console.log(true && false);
    console.log(false && true);
    console.log(true && true);
    console.log(false && false)

    // OR
    console.log(false || false)
    console.log(false || true);
    console.log(true || false);
    console.log(true || true);

